package tasod.graphics;

/**
 * Class representing a 3-dimension floating point vector.
 * @author Arnaud Paré-Vogt
 *
 */
public class Vec3f {
	public float x;
	public float y;
	public float z;
	
	/**
	 * Creates a Vec3f representing the vector zero
	 */
	public Vec3f(){
		x = 0;
		y = 0;
		z = 0;
	}
}
